// Implicit Conversion - automatic type conversion

let result;

result = '3' + 2; 
console.log(result)

let result1;

result1 = '3' + true; 
console.log(result1)

// Explicit Conversion - manual type conversion

let result2;

// string to number
result2 = Number('324');
console.log(result2);

let result3;
result = Number(null);
console.log(result3);

let result4;
result = parseInt('20.01');
console.log(result4);
